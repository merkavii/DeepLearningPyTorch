# کمبود حافظه (OOM - Out of Memory): حجم مدل و اندازه بچ (Batch Size) اون‌قدر بزرگ هست که روی حافظه (VRAM) یک کارت گرافیک جا نمی‌شه.

# زمان آموزش طولانی: آموزش مدل روی یک کارت گرافیک ممکنه هفته‌ها یا ماه‌ها طول بکشه!

# راه حل صنعت دیپ‌لرنینگ برای این چالش، توزیع کردن محاسبات (Distributed Training) روی چندین کارت گرافیک (مثلاً ۲، ۴ یا حتی هزاران GPU همزمان) هست.

# دو استراتژی اصلی برای موازی‌سازی
# در پایتورچ دو روش کلی برای این کار داریم:


# ۱. موازات داده (Data Parallelism)
# در این روش، یک کپی کامل از مدل روی تک‌تک کارت‌های گرافیک قرار می‌گیره.
# حالا دیتای بزرگ شما (مثلاً یک بچ ۱۲۸ تایی) تقسیم می‌شه: ۳۲ تا داده می‌ره روی جی پی یو 0، ۳۲ تا روی جی پی یو 1 و...
# هر کارت گرافیک خطای خودش رو حساب می‌کنه و در نهایت گرادینت‌ها با هم جمع (Average) می‌شن تا وزن‌های مدل آپدیت بشن.

# بزار قدیمی: nn.DataParallel (ساده بود ولی چون همه چیز رو روی یک GPU اصلی مدیریت می‌کرد، گلوگاه سرعت می‌شد).
# ابزار مدرن و استاندارد: DDP (Distributed Data Parallel). این ابزار برای هر کارت گرافیک یک پردازش (Process) کاملاً مجزا باز می‌کنه.
# هیچ ارتباط اضافی بین کارت‌ها نیست مگر در لحظه آخر برای جمع کردن گرادینت‌ها. این روش فوق‌العاده سریع و بهینه است.


# ۲. موازات مدل (Model Parallelism / Tensor Parallel)
# اگر خودِ مدل اون‌قدر غول‌آسا باشه که حتی کپی کردنش روی یک کارت گرافیک هم ممکن نباشه (مثل مدل‌های چند صد میلیارد پارامتری GPT)،
# لایه‌های مدل رو تیکه‌تیکه می‌کنیم! مثلاً لایه ۱ تا ۱۰ می‌ره روی جی پی یو 0، لایه ۱۱ تا ۲۰ می‌ره روی جی پی یو 1.


# یک کد نمونه ساده برای راه‌اندازی DDP در پایتورچ

import torch
import torch.nn as nn
import torch.distributed as dist
from torch.nn.parallel import DistributedDataParallel as DDP

def setup_and_train(rank, world_size):
    # rank: شماره کارت گرافیک فعلی (مثلاً 0 یا 1)
    # world_size: تعداد کل کارت‌های گرافیک
    
    # ۱. راه‌اندازی محیط توزیع‌شده پایتورچ
    dist.init_process_group("nccl", rank=rank, world_size=world_size)
    torch.cuda.set_device(rank)
    
    # ۲. ساخت مدل و فرستادنش روی GPU اختصاصیِ همین پردازش
    model = nn.Linear(768, 10).to(rank)
    
    # ۳. غلاف‌گذاری مدل با DDP برای مدیریت اتوماتیک موازی‌سازی وزن‌ها
    model = DDP(model, device_ids=[rank])
    
    # حالا لوپ آموزش معمولی اجرا می‌شه، ولی پایتورچ پشت صحنه
    # داده‌ها و گرادینت‌ها رو بین تمام کارت‌ها هماهنگ می‌کنه!
    print(f"GPU {rank} is ready and training in parallel!")
    
    dist.destroy_process_group()
"""
====================================================================================================
🚀 تکنیک شتاب‌دهی آموزش با دقت ترکیبی خودکار (Automatic Mixed Precision - AMP) مخصوص کارت گرافیک RTX 4060
====================================================================================================

❓ این مبحث اصلاً چیست؟
به طور پیش‌فرض، پایتورچ تمام محاسبات لایه‌ها، فعال‌سازها و گرادینت‌ها را با دقت اعشاری ۳۲ بیتی (Float32) انجام می‌دهد 
که حافظه زیادی از کارت گرافیک (VRAM) را اشغال می‌کند و سرعت محاسبات را محدود می‌سازد.

در تکنیک Mixed Precision، ما به صورت هوشمند نوع داده‌ها را ترکیب می‌کنیم:
  1. لایه‌های حساس (مثل محاسبه تابع هزینه / Loss) روی همان دقت ۳۲ بیتی (Float32) باقی می‌مانند تا دقت مدل افت نکند.
  2. لایه‌های غیرحساس (مثل ضرب‌های ماتریسی بزرگ در لایه‌های خطی و کانولوشنی ViT) به دقت ۱۶ بیتی (Float16 یا BFloat16) 
     تبدیل می‌شوند تا هسته‌های سخت‌افزاری Tensor Cores در کارت گرافیک RTX 4060 فعال شوند.

💎 مزایای فوق‌العاده AMP روی سیستم شما (RTX 4060):
  * افزایش سرعت: سرعت آموزش و ارزیابی مدل بسته به معماری تا ۲ برابر سریع‌تر می‌شود.
  * بهینه‌سازی حافظه (VRAM): مصرف حافظه گرافیکی تقریباً نصف می‌شود! این یعنی می‌توانید بدون گرفتن خطای معروف 
    Out of Memory (OOM)، اندازه بچ (Batch Size) بزرگتری را برای آموزش مدل‌های سنگین‌تر مثل ViT انتخاب کنید.

🛡️ دو ابزار کلیدی پایتورچ در این بخش:
  * `torch.amp.autocast`: بخش‌هایی از کدهای forward را به صورت خودکار به Float16 کست (Cast) می‌کند.
  * `torch.amp.GradScaler`: از آنجا که گرادینت‌های ۱۶ بیتی بسیار کوچک هستند و ممکن است صفر شوند (Underflow)، 
    این ابزار قبل از آپدیت وزن‌ها، گرادینت‌ها را اسکیل (بزرگ) می‌کند تا جریان یادگیری قطع نشود.
====================================================================================================
"""

import torch
import torch.nn as nn
from torch.utils.data import TensorDataset, DataLoader

# ۱. تعریف اسکیلر گرادینت در ابتدای کد
scaler = torch.amp.GradScaler()

model = nn.Linear(768, 10).cuda()
optimizer = torch.optim.Adam(model.parameters(), lr=0.001)
loss_fn = nn.MSELoss()

# داده‌های فرضی
X = torch.randn(100, 5)      # 100 نمونه، 5 ویژگی
y = torch.randn(100, 1)      # مقدار هدف

# ساخت Dataset
dataset = TensorDataset(X, y)

# ساخت DataLoader
dataloader = DataLoader(
    dataset,
    batch_size=16,
    shuffle=True
)


# در داخل لوپ آموزش:
for inputs, targets in dataloader:
    inputs, targets = inputs.cuda(), targets.cuda()
    
    optimizer.zero_grad()
    
    # ۲. جادو از اینجا شروع می‌شود: فعال‌سازی اتوکست برای لایه‌ها
    with torch.amp.autocast(device_type="cuda", dtype=torch.float16):
        outputs = model(inputs)
        loss = loss_fn(outputs, targets)
        
    # ۳. پس‌پخش خطا و آپدیت وزن‌ها از طریق اسکیلر
    scaler.scale(loss).backward() # اسکیل کردن خطا برای جلوگیری از محو شدن
    scaler.step(optimizer)        # آپدیت وزن‌ها
    scaler.update()               # آماده‌سازی اسکیلر برای بچ بعدی
    # GradScaler جلوی کوچک شدن بیش از حد گرادیان‌ها (Underflow) را هنگام استفاده از float16 می‌گیرد.
    
    
